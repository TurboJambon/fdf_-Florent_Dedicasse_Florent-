#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = ft_strlen(s);
	while (s[i] != s[0])
	{
		if (s[i] == c)
			return ((char*)&s[i]);
		i--;
	}
	if (s[i] == c)
		return ((char*)&s[i]);
	if (c == '\0')
		return ((char*)&s[ft_strlen(s) + 1]);
	return (NULL);
}
