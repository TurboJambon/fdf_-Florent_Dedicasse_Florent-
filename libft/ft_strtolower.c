#include "libft.h"

char	*ft_strtolower(char *str)
{
	int		i;
	char	*new;

	i = 0;
	new = ft_strnew(ft_strlen(str));
	while (str[i])
	{
		new[i] = ft_tolower(str[i]);
		i++;
	}
	return (new);
}
